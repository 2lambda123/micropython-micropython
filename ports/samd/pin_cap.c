// Test, wether the given pin is defined and has signals for sercom.
// If that applies return the alt_fct and pad_nr.
// If no, the alt_fct is set to 0xff
// Alternatively, an error could be raised.
pad_config_t is_sercom_n(int pin_id, uint8_t sercom_nr) {
    int i;
    for (i = 0; i < ARRAY_SIZE(pin_cap_table); i++) {
        if (pin_cap_table[i].pin_id == pin_id) { // Pin match
            if ((pin_cap_table[i].sercom1 >> 4) == sercom_nr) {
                return (pad_config_t) {ALT_FCT_SERCOM1, pin_cap_table[i].sercom1 & 0x0f};
            } else if ((pin_cap_table[i].sercom2 >> 4) == sercom_nr) {
                return (pad_config_t) {ALT_FCT_SERCOM2, pin_cap_table[i].sercom2 & 0x0f};
            } else {
                break;
            }
        }
    }
    mp_raise_ValueError(MP_ERROR_TEXT("no/wrong UART pin"));
}


void pin_cap_set_mux(int pin, uint8_t mux) {
    int pin_grp = pin / 32;
    int port_grp = (pin % 32) / 2;
    PORT->Group[pin_grp].PINCFG[pin % 32].bit.PMUXEN = 1; // Enable Mux
    if (pin & 1) {
        PORT->Group[pin_grp].PMUX[port_grp].bit.PMUXO = mux;
    } else {
        PORT->Group[pin_grp].PMUX[port_grp].bit.PMUXE = mux;
    }
}
